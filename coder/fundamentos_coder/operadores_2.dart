void main(List<String> args) {
//Operadores de Atribuição (Binário/Infix)

  int a = 3;
  a = 4;
  a += 1;
  print(a);

  a *= 2;
  print(a);

  //Operadores de Relacionais (Binário/Infix)--> resultado sempre é Bool
  print(3 > 2);//true
  print(3 >= 2);//true
  print(3 != 2);//true
  print(3 == '3');//false

}
